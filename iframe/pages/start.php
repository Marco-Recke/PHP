<p>Im DFG-Projekt Open-Access-Statistik wird eine Infrastruktur zur Aggregation und Standardisierung von Nutzungsdaten
aus institutionellen Repositorien, Linkresolvern und Lizenzservern aufgebaut.</p>
<p>Um eine große Verbreitung der Projektergebnisse zu gewährleisten, werden zum einen Empfehlungen in das
DINI-Zertifikat übernommen. Zum anderen werden Repositorienbetreibern Richtlinien, Hilfedokumente und
Softwarepakete zur Verfügung gestellt, damit diese standardisierte Nutzungsdaten erfassen und aussagekräftige
Statistiken bereitstellen können.</p>